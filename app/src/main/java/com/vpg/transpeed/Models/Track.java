package com.vpg.transpeed.Models;

public class Track {

    String order_status_details_id, status, statusDate, statusTime;

    public String getOrder_status_details_id() {
        return order_status_details_id;
    }

    public void setOrder_status_details_id(String order_status_details_id) {
        this.order_status_details_id = order_status_details_id;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getStatusDate() {
        return statusDate;
    }

    public void setStatusDate(String statusDate) {
        this.statusDate = statusDate;
    }

    public String getStatusTime() {
        return statusTime;
    }

    public void setStatusTime(String statusTime) {
        this.statusTime = statusTime;
    }
}
